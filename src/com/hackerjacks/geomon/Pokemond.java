package com.hackerjacks.geomon;

public class Pokemond {

	public String Name;
	public int Number;
	public String Description;
	public String Type;
	public String Height;
	public String Weight;
	
	public Pokemond(String name, int number, String description, String type, String height, String weight)
	{
		Name = name;
		Number = number;
		Description = description;
		Type = type;
		Height = height;
		Weight = weight;
	}

}
